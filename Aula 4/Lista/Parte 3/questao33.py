dia = int(input('Insira o número do dia: '))

if dia == 1:
    print(f'O dia {dia} corresponde à segunda')
elif dia == 2:
    print(f'O dia {dia} corresponde à terça')
elif dia == 3:
    print(f'O dia {dia} corresponde à quarta')
elif dia == 4:
    print(f'O dia {dia} corresponde à quinta')
elif dia == 5:
    print(f'O dia {dia} corresponde à sexta')
elif dia == 6:
    print(f'O dia {dia} corresponde à sábado')
elif dia == 7:
    print(f'O dia {dia} corresponde à domingo')
else:
    print('Dia inválido')