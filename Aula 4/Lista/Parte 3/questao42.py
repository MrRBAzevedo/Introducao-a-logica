trimestre = int(input('Insira o número do trimestre: '))

if trimestre == 1:
    print('Janeiro, fevereiro, março')
elif trimestre == 2:
    print('Abril, maio, junho')
elif trimestre == 3:
    print('Julho, agosto, setembro')
elif trimestre == 4:
    print('Outubro, novembro, dezembro')
else:
    print('Trimestre inválido')

